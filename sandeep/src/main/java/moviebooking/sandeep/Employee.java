package moviebooking.sandeep;

public class Employee {

	private String name;
	private String role;
	
	Employee()
	{
		
	}
	Employee(String name,String role)
	{
		
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getRole() {
		return role;
	}
	public void setRole(String role) {
		this.role = role;
	}
	
	
	
}
